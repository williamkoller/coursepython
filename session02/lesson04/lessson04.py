"""
Tipos de dados
str - string - textos 'Assim' "Assim"
int - int - 12456 0 -10 -20 50 6000 -15000
float - real/ponto - 10.50 -10.50 1.5
bool - booleano/lógico - True/False
"""

print('William', type('William'))
print(32, type(32))
print(1.85, type(1.85))
print(10 == 10, type(10 == 10))
print('l' == 'L', type('l' == 'L'))
print(bool(''))
print('william', type('william'), bool('william'))

